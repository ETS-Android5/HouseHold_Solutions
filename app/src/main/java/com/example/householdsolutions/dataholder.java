package com.example.householdsolutions;

public class dataholder {

    String name,Id,mailid,mobileNumber,password,experience,address;

    public dataholder(String name, String id, String mailid, String mobileNumber, String password, String experience, String address) {
        this.name = name;
        Id = id;
        this.mailid = mailid;
        this.mobileNumber = mobileNumber;
        this.password = password;
        this.experience = experience;
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getMailid() {
        return mailid;
    }

    public void setMailid(String mailid) {
        this.mailid = mailid;
    }

    public String getMobileNumber() {
        return mobileNumber;
    }

    public void setMobileNumber(String mobileNumber) {
        this.mobileNumber = mobileNumber;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getExperience() {
        return experience;
    }

    public void setExperience(String experience) {
        this.experience = experience;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
